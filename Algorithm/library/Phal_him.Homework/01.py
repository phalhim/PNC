import tkinter as tk
root = tk.Tk()
root.geometry("600x600")
frame = tk.Frame()
frame.master.title("HELLO PNC")
canvas =tk.Canvas(frame)
canvas.create_oval(20,10,200,100,fill="#ffffff")
canvas.create_oval(150,10,60,100,fill="#0000ff")
canvas.create_oval(550,10,350,100,fill="#ffffff")
canvas.create_oval(400,10,500,100,fill="#0000ff")
canvas.create_oval(300,300,250,200,fill="#ff0000")
canvas.create_rectangle(90,400,500,450,fill="#ff0000")
canvas.create_rectangle(50,300,100,450,fill="#ff0000")
canvas.create_rectangle(500,350,550,450,fill="#ff0000")
canvas.pack(expand=True, fill="both")
frame.pack(expand=True, fill="both")
root.mainloop()