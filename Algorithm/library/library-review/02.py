import tkinter as tk
root = tk.Tk()
root.geometry("600x600")
frame = tk.Frame()
frame.master.title("Dog")
canvas = tk.Canvas(frame)
canvas.create_rectangle(100, 10, 10, 100, fill="#F08080")
canvas.create_rectangle(120, 10, 210, 100, fill="#FA8072")
canvas.create_rectangle(230, 10, 320, 100, fill="#E9967A")
canvas.create_rectangle(340, 10, 430, 100, fill="#E9967A")
canvas.create_rectangle(450, 10, 550, 100, fill="#E9967A")
canvas.pack(expand=True, fill="both")
frame.pack(expand=True, fill="both")
root.mainloop()